package com.company;

public class Main {

    public static void main(String[] args) {
        int i = 500;
        for (; i < 650; i += 10) {
            System.out.println(i + "for");
        }

        i = 500;
        while (i < 650) {
            System.out.println(i + "while");
            i += 10;
        }


        i = 500;
        do {
            System.out.println(i + "do-while");
            i += 10;
        } while (i < 650);
        }
        }






